    private void Analyze(String Expression, boolean Inference) {
        try {
            jAnalyzeTP.setText("");
            
            AnalyzeMessager.AnalyzeMessage("PARSING...");
            SymbolNode n = Parser.Parse(Expression, null, true);
            
            AnalyzeMessager.AnalyzeMessage("");
            AnalyzeMessager.AnalyzeMessage("CALCULATING TRUTHTABLE...");
            n.CalculateTruthTable(true);
            
            TruthTableFrame ttf = new TruthTableFrame(n);
            ttf.show();
            
            AnalyzeMessager.AnalyzeMessage("");
            
            if (Inference) {
                if (n.IsTautology()) {
                    AnalyzeMessager.AnalyzeMessage(n.GetExpression()+" ist eine Tautolgie, d.h. der Schluss ist gültig.");
                    SymbolNode prn = Parser.Parse(GetInfPremConjunction(), null, false);
                    prn.CalculateTruthTable(false);
                    if (prn.IsAntilogy())
                        AnalyzeMessager.AnalyzeMessage("Aber die Prämissen enthalten einen Widerspruch, darum kann\naussagenlogisch Beliebiges gefolgert werden.");
                    else
                        AnalyzeMessager.AnalyzeMessage("Die Prämissen sind in Ordnung, sie schliessen sich nicht gegenseitig aus.");
                }
                else
                    AnalyzeMessager.AnalyzeMessage(n.GetExpression()+" ist keine Tautolgie, d.h. der Schluss ist ungültig.");
                
            }
            else {
                if (n.IsTautology())
                    AnalyzeMessager.AnalyzeMessage(n.GetExpression()+" ist eine Tautologie");
                else if (n.IsAntilogy())
                    AnalyzeMessager.AnalyzeMessage(n.GetExpression()+" ist eine Antilogie");
                else
                    AnalyzeMessager.AnalyzeMessage(n.GetExpression()+" ist weder Tautologie noch Antilogie");
            }
        }
        catch (SyntaxErrorException e) {
            OptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
